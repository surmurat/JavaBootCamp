public class Kitchen{
    private double length;
    private double width;
    private double squareFootage;
    private String refrigeratorType;
    private String furnishType;

    public double getLength() {
        return length;
    }

    public void setLength(double length) {
        this.length = length;
    }

    public double getWidth() {
        return width;
    }

    public void setWidth(double width) {
        this.width = width;
    }

    public double getSquareFootage() {
        return squareFootage;
    }

    public void setSquareFootage(double squareFootage) {
        this.squareFootage = squareFootage;
    }

    public String getRefrigeratorType() {
        return refrigeratorType;
    }

    public void setRefrigeratorType(String refrigeratorType) {
        this.refrigeratorType = refrigeratorType;
    }

    public String getFurnishType() {
        return furnishType;
    }

    public void setFurnishType(String furnishType) {
        this.furnishType = furnishType;
    }
}
