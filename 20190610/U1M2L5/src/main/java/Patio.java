public class Patio{

    private String brand;
    private int numOfThePcs;
    private int numOfSeats;
    private boolean uvResistant;
    private String tableTop;

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public int getNumOfThePcs() {
        return numOfThePcs;
    }

    public void setNumOfThePcs(int numOfThePcs) {
        this.numOfThePcs = numOfThePcs;
    }

    public int getNumOfSeats() {
        return numOfSeats;
    }

    public void setNumOfSeats(int numOfSeats) {
        this.numOfSeats = numOfSeats;
    }

    public boolean isUvResistant() {
        return uvResistant;
    }

    public void setUvResistant(boolean uvResistant) {
        this.uvResistant = uvResistant;
    }

    public String getTableTop() {
        return tableTop;
    }

    public void setTableTop(String tableTop) {
        this.tableTop = tableTop;
    }
}
